!               SIGN ( X(S-1)*A**(S-1) + ... + X(1)*A + X(0) )

!               WHERE 0 .LE. X(I) .LT. A FOR I=0,...,S-1.

!    I1MACH( 7) = A, THE BASE.

!    I1MACH( 8) = S, THE NUMBER OF BASE-A DIGITS.

